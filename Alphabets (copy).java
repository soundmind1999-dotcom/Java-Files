public class Alphabets {

    public static void main(String[] args) {

        System.out.println("Uppercase and Lowercase Letters:");

        
        for (char letter = 'A'; letter <= 'Z'; letter++) {
            System.out.print(letter + " ");
        }

        System.out.println(); 
       
        for (char letter = 'a'; letter <= 'z'; letter++) {
            System.out.print(letter + " ");
        }
    }
}
